def words_order(text, words):
    string = text.split(' ')
    unique_words = set(words)
    if words == [] or string == []:
        return False
    if len(unique_words) != len(words):
        return False
    ans = [i for i in string if i in words]
    if words == ans:
        return True
    else:
        return False


if __name__ == '__main__':
    print("Example:")
    print(words_order('hi world im here', ['world', 'here']))

